package Theatre;

public class Person {
    private  String name,surname,mail;

    public Person(String name,String surname,String mail){
        this.name = name;
        this.surname = surname;
        this.mail = mail;

    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public String getMail() {
        return mail;
    }

}



